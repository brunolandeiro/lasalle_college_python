#Loops
#While
print("---WHILE---")
count = 0;
while count < 3:
    print(count)
    count += 1

#For
print("\nfor i in range(3)")
for i in range(3):
    print(i)

print("\nfor i in range(1, 4)")
for i in range(1, 4):
    print(i)

print("\nfor i in range(1, 6, 2)")
for i in range(1, 6, 2):
    print(i)

print("\nfor i in range(10, -10, -2)")
for i in range(10, -10, -2):
    print(i)

#foreach
print("\nfor item in \"Hello world\"")
for item in "Hello world":
    print(item)
